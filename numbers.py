import math
#gives access to extra math functions

#prints remainder of the result
print(10 % 3)

#prints absolute value of the result
print(abs(-3))

#same as '3 ** 2'
print(pow(3, 2))

#prints largest value
print(max(3, 8))

#prints smallest value
print(min(3, 8))

#rounds value
print(round(3.2))
print(round(3.5))

#prints square root of value
print(math.sqrt(36))

#prints lowest value
print(math.floor(3.7))

#prints round number up regardless of decimal
print(math.ceil(3.7))